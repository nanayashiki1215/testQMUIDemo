- (void)cornerWithRados:(CGFloat)rados byRoundingCorners:(UIRectCorner)corners
{
    UIBezierPath *maskPath = [UIBezierPath bezierPathWithRoundedRect:self.bounds      byRoundingCorners:corners    cornerRadii:CGSizeMake(rados, rados)];
    CAShapeLayer *maskLayer = [[CAShapeLayer alloc] init];
    maskLayer.frame = self.bounds;
    maskLayer.path = maskPath.CGPath;
    self.layer.mask = maskLayer;
}


- (UIView *)createAlpaha:(CGFloat)alpha
{
    UIView *alphaView = [[UIView alloc] init];
    alphaView.size = self.size;
    alphaView.backgroundColor = [UIColor blackColor];
    alphaView.alpha = alpha;
    [self addSubview:alphaView];
    return alphaView;
}


- (void)locaAnimationWithType:(NSInteger)type rados:(CGFloat)rados repeatCount:(NSInteger)repeatCount duration:(CFTimeInterval)duration
{
    CALayer *lbl = [self layer];
    CGPoint posLbl = [lbl position];
    CGPoint y = CGPointMake((type == 1 ? posLbl.x :posLbl.x - rados),(type == 1 ? posLbl.y - rados : posLbl.y ));
    CGPoint x = CGPointMake((type == 1? posLbl.x : posLbl.x + rados),( type == 1 ? posLbl.y + 10 : posLbl.y));
    CABasicAnimation * animation = [CABasicAnimation animationWithKeyPath:@"position"];
    [animation setTimingFunction:[CAMediaTimingFunction
                                  functionWithName:kCAMediaTimingFunctionEaseInEaseOut]];
    [animation setFromValue:[NSValue valueWithCGPoint:x]];
    [animation setToValue:[NSValue valueWithCGPoint:y]];
    [animation setAutoreverses:YES];
    if (duration == 0) {
        duration = 0.08;
    }
    [animation setDuration:duration];
    if (repeatCount == 0) {
        repeatCount = 3;
    }
    [animation setRepeatCount:repeatCount];
    [lbl addAnimation:animation forKey:nil];
}

- (void)jitterAnimationWithRotation:(CGFloat)rotation
{
    if (rotation == 0) {
        rotation = 0.03;
    }
    CGAffineTransform t1 = CGAffineTransformMakeRotation(rotation);
    CGAffineTransform t2 = CGAffineTransformMakeRotation(-rotation);
    self.transform = t1;
    [UIView animateWithDuration:0.1 delay:0.0 options:UIViewAnimationOptionAutoreverse|UIViewAnimationOptionRepeat animations:^{
        //[UIView setAnimationRepeatCount:12.0];
        self.transform = t2;
    } completion:^(BOOL finished){
        
        [UIView animateWithDuration:0.2 delay:0.0 options:UIViewAnimationOptionCurveEaseInOut animations:^
         {
             CGAffineTransform t = CGAffineTransformMakeScale(1,1);
             self.transform = t;
         } completion:^(BOOL finished)
         {
             NSLog(@"完成");
         }];
    }];
}


